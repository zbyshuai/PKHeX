    /// <summary>
    /// Converts a Generation 4 value to Unicode character.
    /// </summary>
    /// <param name="val">Encoded value.</param>
    /// <returns>Decoded value (unicode).</returns>
    public static ushort ConvertValue2CharG4(ushort val)
    {
        if (val <= TableINTEnd)
            return TableINT[val];
        if (val is <= TableKOREnd and >= TableKORStart)
            return TableKOR[val - TableKORStart];
        return NUL;
    }

    /// <summary>
    /// Converts a Unicode character to Generation 4 value.
    /// </summary>
    /// <param name="chr">Decoded value (unicode).</param>
    /// <returns>Encoded value.</returns>
    public static ushort ConvertChar2ValueG4(ushort chr)
    {
        var c = (char)chr;
        var index = TableINT.IndexOf(c);
        if (index >= 0)
            return (ushort)index;
        index = TableKOR.IndexOf(c);
        if (index >= 0)
            return (ushort)(index + TableKORStart);
        return SaveInvalidAs;
    }

    /// <summary>
    /// Strips diacritics on Gen1-4 French species names.
    /// </summary>
    /// <param name="input">String to clean</param>
    /// <returns>Cleaned string</returns>
    /// <remarks>Only 4 characters are accented in Gen1-4</remarks>
    public static void StripDiacriticsFR4(Span<char> input)
    {
        foreach (ref var c in input)
        {
            // È É Ê Ï
            // C8 C9 CA map to 'E'
            // CF map to 'I'
            var delta = (uint)(c - 'È');
            if (delta > 7)
                continue;
            if (delta < 3)
                c = 'E';
            else if (delta == 7)
                c = 'I';
        }
    }

